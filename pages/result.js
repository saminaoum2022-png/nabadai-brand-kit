import { useEffect, useState } from "react";

export default function Result() {
  const [kit, setKit] = useState(null);

  useEffect(() => {
    const data = localStorage.getItem("brandKit");
    if (data) setKit(JSON.parse(data));
  }, []);

  if (!kit) return (
    <div style={{ minHeight: "100vh", background: "#0a0a0f", display: "flex", alignItems: "center", justifyContent: "center" }}>
      <p style={{ color: "#00d4ff" }}>Loading your brand kit...</p>
    </div>
  );

  return (
    <div style={{ minHeight: "100vh", background: "#0a0a0f", color: "#fff", fontFamily: "sans-serif", padding: "40px 20px" }}>
      <div style={{ maxWidth: 700, margin: "0 auto" }}>

        {/* Header */}
        <div style={{ textAlign: "center", marginBottom: 48 }}>
          <p style={{ color: "#00d4ff", letterSpacing: 4, fontSize: 12, marginBottom: 8 }}>NABADAI — AI BRAND IDENTITY</p>
          <h1 style={{ fontSize: 42, fontWeight: 800, marginBottom: 8 }}>{kit.brandName}</h1>
          <p style={{ color: "#888" }}>Your custom brand kit is ready</p>
        </div>

        {/* Logo Concepts */}
        {kit.logos && (
          <div style={{ background: "#1a1a2e", borderRadius: 16, padding: 32, marginBottom: 24, border: "1px solid #00d4ff22" }}>
            <h2 style={{ color: "#00d4ff", marginBottom: 20, fontSize: 14, letterSpacing: 3 }}>AI LOGO CONCEPTS</h2>
            <div style={{ display: "grid", gridTemplateColumns: "repeat(3, 1fr)", gap: 16 }}>
              {kit.logos.map((url, i) => (
                <div key={i} style={{ textAlign: "center" }}>
                  <img src={url} alt={`Logo concept ${i + 1}`} style={{ width: "100%", borderRadius: 12, background: "#fff", padding: 8 }} />
                  <p style={{ color: "#888", fontSize: 12, marginTop: 8 }}>Concept {i + 1}</p>
                </div>
              ))}
            </div>
          </div>
        )}

        {/* Slogans */}
        <div style={{ background: "#1a1a2e", borderRadius: 16, padding: 32, marginBottom: 24, border: "1px solid #00d4ff22" }}>
          <h2 style={{ color: "#00d4ff", marginBottom: 20, fontSize: 14, letterSpacing: 3 }}>SLOGAN OPTIONS</h2>
          {[kit.slogan1, kit.slogan2, kit.slogan3].map((s, i) => (
            <div key={i} style={{ padding: "16px 20px", background: "#0a0a0f", borderRadius: 8, marginBottom: 12, borderLeft: "3px solid #00d4ff" }}>
              <p style={{ margin: 0, fontSize: 18, fontStyle: "italic" }}>"{s}"</p>
            </div>
          ))}
        </div>

        {/* Colors */}
        <div style={{ background: "#1a1a2e", borderRadius: 16, padding: 32, marginBottom: 24, border: "1px solid #00d4ff22" }}>
          <h2 style={{ color: "#00d4ff", marginBottom: 20, fontSize: 14, letterSpacing: 3 }}>COLOR PALETTE</h2>
          <div style={{ display: "flex", gap: 12, flexWrap: "wrap" }}>
            {kit.colors.map((color, i) => (
              <div key={i} style={{ textAlign: "center" }}>
                <div style={{ width: 80, height: 80, borderRadius: 12, background: color, marginBottom: 8, boxShadow: `0 0 20px ${color}44` }} />
                <p style={{ margin: 0, fontSize: 12, color: "#888" }}>{kit.colorNames[i]}</p>
                <p style={{ margin: 0, fontSize: 11, color: "#555" }}>{color}</p>
              </div>
            ))}
          </div>
        </div>

        {/* Typography */}
        <div style={{ background: "#1a1a2e", borderRadius: 16, padding: 32, marginBottom: 24, border: "1px solid #00d4ff22" }}>
          <h2 style={{ color: "#00d4ff", marginBottom: 20, fontSize: 14, letterSpacing: 3 }}>TYPOGRAPHY</h2>
          {kit.fonts.map((font, i) => (
            <div key={i} style={{ padding: "12px 20px", background: "#0a0a0f", borderRadius: 8, marginBottom: 12 }}>
              <p style={{ margin: 0, color: "#fff" }}>{font}</p>
            </div>
          ))}
        </div>

        {/* Brand Details */}
        <div style={{ background: "#1a1a2e", borderRadius: 16, padding: 32, marginBottom: 24, border: "1px solid #00d4ff22" }}>
          <h2 style={{ color: "#00d4ff", marginBottom: 20, fontSize: 14, letterSpacing: 3 }}>BRAND PROFILE</h2>
          {[
            { label: "Brand Archetype", value: kit.brandArchetype },
            { label: "Brand Personality", value: kit.brandPersonality },
            { label: "Target Audience", value: kit.targetAudience },
            { label: "Tone of Voice", value: kit.toneOfVoice },
          ].map(({ label, value }) => (
            <div key={label} style={{ marginBottom: 20 }}>
              <p style={{ color: "#00d4ff", fontSize: 12, letterSpacing: 2, marginBottom: 6 }}>{label.toUpperCase()}</p>
              <p style={{ color: "#ccc", margin: 0, lineHeight: 1.6 }}>{value}</p>
            </div>
          ))}
        </div>

        {/* Footer */}
        <div style={{ textAlign: "center", padding: 32 }}>
          <p style={{ color: "#555", fontSize: 12 }}>Generated by NabadAi — AI-Powered Digital Agency</p>
          <p style={{ color: "#00d4ff", fontSize: 12 }}>nabadai.com</p>
        </div>

      </div>
    </div>
  );
}
